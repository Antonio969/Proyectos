package Double;



public class Tipo_Double {
    public static void main(String[] args) {
        double salario = 123432.22;

        System.out.println(salario);

        int edad = 24;

        System.out.println(edad);

        double salario_mitad = salario/2;
        
        System.out.println(salario_mitad);

        int division = edad/7;

        System.out.println(division);



    }
}
